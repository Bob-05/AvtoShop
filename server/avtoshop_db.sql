-- =============================================
-- 1. Создание базы данных
-- =============================================
CREATE DATABASE IF NOT EXISTS avtoshop_db
CHARACTER SET utf8mb4
COLLATE utf8mb4_unicode_ci;

USE avtoshop_db;

-- =============================================
-- 2. Таблица администраторов
-- =============================================
CREATE TABLE IF NOT EXISTS admin (
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT 'Уникальный идентификатор администратора',
    email VARCHAR(255) NOT NULL UNIQUE COMMENT 'Email для входа в админ-панель',
    password_hash VARCHAR(255) NOT NULL COMMENT 'Хеш пароля (bcrypt)',
    role ENUM('admin') DEFAULT 'admin' COMMENT 'Роль пользователя',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT 'Дата и время создания записи'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Учетные данные администраторов';

-- =============================================
-- 3. Таблица услуг
-- =============================================
CREATE TABLE IF NOT EXISTS services (
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT 'Уникальный идентификатор услуги',
    name VARCHAR(255) NOT NULL COMMENT 'Название услуги',
    description TEXT NOT NULL COMMENT 'Подробное описание услуги',
    price VARCHAR(100) NOT NULL COMMENT 'Стоимость услуги (в строковом формате)',
    icon_url VARCHAR(512) COMMENT 'Путь к файлу иконки на сервере',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT 'Дата и время создания',
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT 'Дата и время последнего обновления'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Перечень услуг автосервиса';

-- =============================================
-- 4. Таблица отзывов
-- =============================================
CREATE TABLE IF NOT EXISTS reviews (
    id INT AUTO_INCREMENT PRIMARY KEY COMMENT 'Уникальный идентификатор отзыва',
    name VARCHAR(255) NOT NULL COMMENT 'Имя автора отзыва',
    text TEXT NOT NULL COMMENT 'Текст отзыва',
    rating TINYINT NOT NULL COMMENT 'Оценка от 1 до 5',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT 'Дата и время создания отзыва',
    CONSTRAINT chk_rating CHECK (rating >= 1 AND rating <= 5)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='Отзывы клиентов';

